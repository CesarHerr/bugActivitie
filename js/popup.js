// this is for popup desktop
const main = document.querySelector('body');
const cardPopup = document.querySelectorAll('.see-desktop');

cardPopup.forEach((parameter, index) => {
  parameter.addEventListener('click', () => {
    const cards = document.createElement('div');
    cards.className = 'popup-container';

    cards.innerHTML = `
    <div class="cardDesktopPopup">
        <img class="close" src="${objectWorks[index].buttonClose}" alt="">   
        <div class="popup-header">
            <h2>${objectWorks[index].title[0]}</h2>        
            <ul class="popup-skills">
                <li>${objectWorks[index].skills[0]}</li>
                <li>${objectWorks[index].skills[1]}</li>
                <li>${objectWorks[index].skills[2]}</li>
            </ul>
        </div>
        <img class="image-project" src="${objectWorks[index].img[0]}" alt="imagen_project">
        <div class="popup-description">
            <p>${objectWorks[index].description[0]}</p>        
            <a href="${objectWorks[index].btnSeeLive}">
                <button class="btnSeeLive">See Live<img src="${objectWorks[index].img[1]}" alt=""></button>
            </a>    
            <a href="${objectWorks[index].btnSeeSource}">
                <button class="btnSeeSource">See Source<img src="${objectWorks[index].img[2]}" alt=""></button>
            </a>     
        </div>
    </div>`;

    main.insertAdjacentElement('afterBegin', cards);
    const close = document.querySelector('.close');
    close.addEventListener('click', () => {
      main.removeChild(cards);
    });
  });
});

